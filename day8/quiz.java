// 인터페이스 정의
public interface Drawable {
    void draw();  // public abstract가 자동으로 붙음
    void erase();
}

public interface Resizable {
    void resize(double factor);
}

// 여러 인터페이스 구현
public class Photo implements Drawable, Resizable {
    private String filename;
    private int width, height;

    public Photo(String filename, int width, int height) {
        this.filename = filename;
        this.width = width;
        this.height = height;
    }

    @Override
    public void draw() {
        System.out.println(filename + " 사진을 화면에 그립니다.");
    }

    @Override
    public void erase() {
        System.out.println(filename + " 사진을 화면에서 지웁니다.");
    }

    @Override
    public void resize(double factor) {
        width = (int)(width * factor);
        height = (int)(height * factor);
        System.out.println("크기 조정: " + width + "x" + height);
    }
}


// 인터페이스 어떻게 만듬? 
// 인터페이스는 클래스와 유사하지만, 메서드의 구현이 없는 추상 메서드만 포함할 수 있습니다.
// 인터페이스는 클래스가 특정 기능을 제공한다는 것을 명시적으로 나타내기 위해 사용됩니다. 인터페이스는 클래스가 구현해야 하는 메서드의 시그니처를 정의하며, 클래스는 이 인터페이스를 구현하여 해당 메서드를 제공해야 합니다. 인터페이스는 다중 상속이 가능하므로, 하나의 클래스가 여러 인터페이스를 구현할 수 있습니다.
// 인터페이스는 객체 지향 프로그래밍에서 중요한 역할을 합니다. 인터페이스를 사용하면 클래스 간의 결합도를 낮추고, 코드의 유연성과 재사용성을 높일 수 있습니다. 또한, 인터페이스를 통해 다형성을 구현할 수 있어, 다양한 객체가 동일한 인터페이스를 구현하여 일관된 방식으로 상호 작용할 수 있습니다.

// 인터페이스 다중 상속할 때 같은 이름 메소드 어느거를 사용?
// 인터페이스 다중 상속 시, 같은 이름의 메서드가 여러 인터페이스에 정의되어 있을 때, 클래스는 해당 메서드를 구현해야 합니다. 이 경우, 클래스는 어떤 인터페이스의 메서드를 구현할지 명확히 해야 합니다. 일반적으로, 클래스는 해당 메서드를 구현하여 모든 인터페이스에서 사용할 수 있도록 합니다. 만약 두 인터페이스에 동일한 시그니처의 메서드가 있다면, 클래스는 그 메서드를 하나만 구현하면 됩니다. 이때, 클래스는 해당 메서드가 어떤 인터페이스에서 왔는지 구분할 필요 없이, 하나의 구현으로 모든 인터페이스에서 사용할 수 있습니다.
//@override 는 뭐야?
// @Override는 자바에서 메서드가 슈퍼클래스의 메서드를 오버라이드(재정의)하고 있음을 나타내는 어노테이션입니다. 이 어노테이션을 사용하면 컴파일러가 해당 메서드가 실제로 슈퍼클래스의 메서드를 오버라이드하고 있는지 확인할 수 있습니다. 만약 메서드 시그니처가 슈퍼클래스의 메서드와 일치하지 않거나, 슈퍼클래스에 해당 메서드가 존재하지 않는 경우, 컴파일러는 오류를 발생시킵니다. @Override 어노테이션은 코드의 가독성을 높이고, 개발자가 실수로 메서드를 잘못 오버라이드하는 것을 방지하는 데 도움이 됩니다.

// 인터페이스의 추상 메서드는 반드시 구현해야 하는가?
// 네, 인터페이스의 추상 메서드는 반드시 구현해야 합니다. 인터페이스는 메서드의 시그니처만 정의하고, 실제 구현은 해당 인터페이스를 구현하는 클래스에서 제공해야 합니다. 따라서, 인터페이스에 정의된 모든 추상 메서드는 인터페이스를 구현하는 클래스에서 반드시 구현되어야 합니다. 만약 클래스가 인터페이스의 추상 메서드를 구현하지 않으면, 컴파일 오류가 발생합니다. 이는 인터페이스가 특정 기능을 제공한다는 계약을 명확히 하기 위해서입니다.


// 인터페이스 끼리의 상속
// 인터페이스는 다른 인터페이스를 상속할 수 있습니다. 이를 통해 인터페이스 간의 계층 구조를 만들 수 있습니다. 예를 들어, Drawable 인터페이스가 있고, Resizable 인터페이스가 있다면, 새로운 인터페이스인 Shape를 만들어 Drawable과 Resizable을 상속할 수 있습니다. 이렇게 하면 Shape 인터페이스는 Drawable과 Resizable의 모든 메서드를 포함하게 됩니다. 클래스가 Shape 인터페이스를 구현하면, Drawable과 Resizable의 모든 메서드를 구현해야 합니다. 인터페이스 간의 상속은 코드의 재사용성을 높이고, 더 복잡한 기능을 가진 인터페이스를 쉽게 만들 수 있도록 도와줍니다.

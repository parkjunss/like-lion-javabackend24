package day2;

public class VariableTest {
    int age; // 정수형 변수 age 선언, 초기값이 할당되지 않았으므로 기본값 0이 사용됨 필드 속성
    public static void main(String[] args) {
        // 변수 = 데이터를 저장하는 공간, 변수명과 데이터 타입으로 구성
        // 데이터 타입 = 변수에 저장할 수 있는 데이터의 종류를 정의하는 것
        // int = 정수형 데이터 타입, 소수점이 없는 숫자
        // double = 실수형 데이터 타입, 소수점이 있는 숫자
        // String = 문자열 데이터 타입, 문자들의 집합
        // 접근제한자 타입 변수명;
        // 접근제한자 타입 변수명 = 초기값;
        // 지역변수 = 메서드 내부에서 선언된 변수, 메서드가 실행될 때 생성되고 종료될 때 소멸됨
        // 자바에서 변수의 범위는 블록 단위로 결정됨, 블록은 중괄호 {}로 감싸진 영역을 의미
        // 정수형 변수 age 선언 및 초기화 여기서 접근제한자는 생략되어 있는데, 기본적으로 패키지 내에서 접근 가능한 default 접근제한자가 적용된다.
        int age = 25; // 선언과 초기화 동시
        double height = 175.5; // 실수형 변수 height 선언 및 초기화
        String name = "Alice"; // 문자열 변수 name 선언 및 초기화
        if (age > 18) {
            String status = "Adult"; // 지역변수 status 선언 및 초기화, 이 변수는 if 블록 내부에서만 접근 가능
            System.out.println("Status: " + status);
        }

        // System.err.println("Status: " + status); // 컴파일 에러, status 변수는 if 블록 밖에서 접근할 수 없음
        final int MAX_AGE = 120; // 상수 MAX_AGE 선언 및 초기화, final 키워드를 사용하여 값을 변경할 수 없도록 함
        // MAX_AGE = 150; // 컴파일 에러, final로 선언된 상수는 값을 변경할 수 없음
        // 클래스 앞에 final 키워드를 사용하면 해당 클래스를 상속할 수 없도록 할 수 있음, 메서드 앞에 final 키워드를 사용하면 해당 메서드를 오버라이드할 수 없도록 할 수 있음
        // 상속은 기존 클래스의 기능을 확장하여 새로운 클래스를 만드는 객체지향 프로그래밍의 핵심 개념 중 하나, 오버라이드는 부모 클래스의 메서드를 자식 클래스에서 재정의하는 것
        // 오버라이드는 다형성의 한 형태로, 부모 클래스의 메서드를 자식 클래스에서 재정의하여 다양한 형태로 사용할 수 있게 해줌
        // 오버로딩은 같은 이름의 메서드를 여러 개 정의하는 것, 매개변수의 타입이나 개수가 다르면 같은 이름의 메서드를 여러 개 정의할 수 있음

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);

        Person person = new Person("Bob", 30); // Person 클래스의 객체 생성, 생성자 호출
        System.out.println("Person Name: " + person.name); // 객체의 필드에 접근
        System.out.println("Person Age: " + person.age); // 객체의 필드에
        System.out.println();
    }

    public void getAge() {
        System.out.println("Age: " + age); // 클래스의 필드 age는 클래스 내의 모든 메서드에서 접근 가능
    }

    public void setAge(int age) {
        this.age = age; // this 키워드는 현재 객체를 참조, 매개변수 age와 클래스의 필드 age를 구분하기 위해 사용
    }

    // 자바의 타입은 크게 원시 타입과 참조 타입으로 나뉨
    // 원시 타입 = int, double, boolean 등과 같이 기본 데이터 타입을 나타내는 타입
    // 총 8가지 원시 타입이 존재, 각각의 원시 타입은 고유한 크기와 범위를 가지며, 메모리에서 직접 값을 저장하는 방식으로 동작
    // boolean = true 또는 false 값을 가지는 논리형 데이터 타입
    // char = 단일 문자를 나타내는 문자형 데이터 타입, 작은따옴표 ''로 감싸서 표현
    // byte = 8비트 정수형 데이터 타입, -128부터 127까지의 값을 가질 수 있음
    // short = 16비트 정수형 데이터 타입, -32,768부터 32,767까지의 값을 가질 수 있음
    // int = 32비트 정수형 데이터 타입, -2,147,483,648부터 2,147,483,647까지의 값을 가질 수 있음
    // float = 32비트 실수형 데이터 타입, 소수점 이하 7자리까지의 정밀도를 가짐
    // long = 64비트 정수형 데이터 타입, -9,223,372,036,854,775,808부터 9,223,372,036,854,775,807까지의 값을 가질 수 있음   
    // double = 64비트 실수형 데이터 타입, 소수점 이하 15자리까지의 정밀도를 가짐
    // 참조 타입 = 클래스, 인터페이스, 배열 등과 같이 객체를 참조하는 타입
    // 참조 타입은 메모리에서 객체의 주소를 저장하는 방식으로 동작, 참조 타입의 변수는 객체를 참조하는 역할을 하며, 객체의 필드와 메서드에 접근할 수 있음
    // 참조 타입의 예시로는 String, Car, Person 등이 있음
    // String은 자바에서 특별한 참조 타입으로, 문자열을 다루기 위한 클래스임, String 클래스는 불변(immutable) 클래스이므로, 한 번 생성된 문자열 객체는 변경할 수 없음

    // 모든 것이 객체인데 왜 원시 타입이 존재하는가? 원시 타입은 성능과 메모리 효율성을 위해 존재함, 원시 타입은 객체보다 더 적은 메모리를 사용하고, 객체를 생성하는 오버헤드가 없기 때문에, 대량의 데이터를 처리할 때 원시 타입이 더 효율적임
    // 메모리 아낄려고? 원시 타입은 객체보다 더 적은 메모리를 사용하기 때문에, 대량의 데이터를 처리할 때 원시 타입이 더 효율적임, 예를 들어, 수백만 개의 정수를 저장해야 하는 경우, int 배열을 사용하는 것이 Integer 객체 배열을 사용하는 것보다 훨씬 더 효율적임

}
